package model;

/**
 *
 * @author Admin
 */
public class Employees {
    private String name;
    private Boolean gender;

    public Employees() {
    }

    public Employees(String name, Boolean gender) {
        this.name = name;
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getGender() {
        return gender;
    }

    public void setGender(Boolean gender) {
        this.gender = gender;
    }
    
}
